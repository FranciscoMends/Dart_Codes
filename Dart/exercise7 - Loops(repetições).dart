void main() {
  
  //for é usado para Loops definidos
  
  for(int a=0; a<=10; a++){
    //print(a);
  }
  
  for(int b=10; b>=0; b-=1){
    //print(b);
  }
  
  for(int c=0; c<=20; c+=2){
    //print(c);
  }
  
  //--------------------------------------------
  
  
  //while é usado para Loops indefinidos
  
  int x=0;
  while(x<=10){
    print("X= $x");
    x++;
  }
  
  int y=0;
  while(y<=10){
    print("Y= $y");
    y+=2;
  }
  
  //--------------------------------------------
  
  
  //do while é usado para Loops indefinidos
  
  int z=0;
  do {
    print("Z= $z");
    z++;
  }while(z<=10);
   
}